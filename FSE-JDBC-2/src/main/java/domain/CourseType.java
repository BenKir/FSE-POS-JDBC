package domain;

/**
 * Enum, das verschiedene Arten von Kursen darstellt.
 * Jeder Wert im Enum repräsentiert eine spezifische Kategorie oder ein spezifisches Angebot eines Kurses.
 * Diese Kursarten sind relevant für eine Bildungs- oder Trainingseinrichtung, die verschiedene Programme und Angebote hat.
 * BF ist begabtenförderung, ZA ist zusatzangebot ...
 */

public enum CourseType {
    OE, BF, ZA, FF, FS
}
